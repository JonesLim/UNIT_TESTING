package com.jones.unit_testing.core.util

object Calc {
    fun add (a: Int, b: Int): Int {
        return a + b
    }

    fun sub (a: Int, b: Int): Int {
        return a - b
    }

    fun mult (a: Int, b: Int): Int {
        return a * b
    }

    fun div (a: Int, b: Int): Int {
        return a / b
    }
}